package day3_comments_escape_sequence;

public class TabAndNextLine {
    public static void main(String[] args) {
        System.out.println(" This is not tabbed");
        System.out.println("Elnur \t\tBayarmov");
        System.out.println("Elnur \nBayarmov");



        System.out.println("1) Go to parking lot");
        System.out.println(") Go to parking lot");
        System.out.println(") Find the car");

        System.out.println("\n1) Go to parking lot \n 2)Find the car \n 3)Start the car");
    }
}
